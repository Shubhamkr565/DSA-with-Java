package DSA_Array;

public class Multi_dimestional_array {

	
	public static void main(String[] args) {
		
		String[][] name = {{"Ram ", "Krishana "},
		{" Radha ", " Sita"}};
		
		System.out.println(name[0][0] + name[1][1]);
		System.out.println(name[0][1] + name[1][0]);
		
	}
}


//		Output 
		
//		Ram  Sita
//		Krishana  Radha 
